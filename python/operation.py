# string
my_string = "Hello, World!"
print("Created string:", my_string)
first_char = my_string[0]
print("First character:", first_char)
substring = my_string[7:12]
print("Sliced string (from index 7 to 11):", substring)
new_string = my_string + " How are you?"
print("Concatenated string:", new_string)
repeated_string = my_string * 2
print("Repeated string:", repeated_string)

# list

my_list = [1, 2, 3, 4, 5]
print("Created list:", my_list)
first_element = my_list[0]
print("First element:", first_element)
sub_list = my_list[1:4]
print("Sliced list (from index 1 to 3):", sub_list)
new_list = my_list + [6, 7, 8]
print("Concatenated list:", new_list)
repeated_list = my_list * 2
print("Repeated list:", repeated_list)

